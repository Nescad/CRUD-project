    private void JblIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JblIngresarActionPerformed
        Integer cod = Integer.valueOf(txtcod.getText());
        String desc = txtdesc.getText();
        Date fechaini = (Date) jdcfechaini.getDate();
        Date fechafin = (Date) jdcfechafin.getDate();
        Actividad objActividad = new Actividad (cod,desc, fechaini, fechafin);
        ControlActividad objControlActividad = new ControlActividad (objActividad);
        objControlActividad.guardar();
        JOptionPane.showConfirmDialog(null, "Registro Guardado");
        txtcod.setText("");
        txtdesc.setText("");
        jdcfechaini.setDate(null);
        jdcfechafin.setDate(null);
        
    }//GEN-LAST:event_JblIngresarActionPerformed

