    private void JblConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JblConsultarActionPerformed
        Integer cod = Integer.valueOf(txtcod.getText());
        String desc = "";
        Date fechaini = null;
        Date fechafin = null; 
        Actividad objActividad= new Actividad (cod,desc, fechaini, fechafin);
        ControlActividad objControlActividad= new ControlActividad (objActividad);
        objActividad = objControlActividad.consultar();
        cod = objActividad.getCod();
        desc = objActividad.getDescripcion();
        fechaini = objActividad.getFechaini();
        fechafin = objActividad.getFechafin();
        txtcod.setText(String.valueOf(cod));
        txtdesc.setText(desc);
        jdcfechaini.setDate(fechaini);
        jdcfechafin.setDate(fechafin);
    }//GEN-LAST:event_JblConsultarActionPerformed

    private void JblBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JblBorrarActionPerformed
        Integer cod = Integer.valueOf(txtcod.getText());
        Actividad objActividad= new Actividad (cod,"", null, null);
        ControlActividad objControlActividad= new ControlActividad (objActividad);
        objControlActividad.borrar();
        JOptionPane.showConfirmDialog(null, "Registro modificado");
        txtcod.setText("");
        txtdesc.setText("");
        jdcfechaini.setDate(null);
        jdcfechafin.setDate(null);
    }//GEN-LAST:event_JblBorrarActionPerformed

