    private void jbnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnIngresarActionPerformed
        Integer cod = Integer.valueOf(txtcod.getText());
        String titulo = txttitulo.getText();
        Date fechaini = (Date) jdcfechaini.getDate();
        Date fechafin = (Date) jdcfechafin.getDate();
        double costo = Double.valueOf(txtcosto.getText());
        Proyecto objProyecto = new Proyecto (cod,titulo, fechaini, fechafin,costo);
        ControlProyecto objControlProyecto = new ControlProyecto (objProyecto);
        objControlProyecto.guardar();
        JOptionPane.showConfirmDialog(null, "Registro Guardado");
        txtcod.setText("");
        txttitulo.setText("");
        jdcfechaini.setDate(null);
        jdcfechafin.setDate(null);
        txtcosto.setText("");
    }//GEN-LAST:event_jbnIngresarActionPerformed

