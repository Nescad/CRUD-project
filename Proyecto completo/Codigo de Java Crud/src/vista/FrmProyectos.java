    private void jbnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnConsultarActionPerformed
        Integer cod = Integer.valueOf(txtcod.getText());
        String titulo = "";
        Date fechaini = null;
        Date fechafin = null; 
        double costo = 0;
        Proyecto objProyecto= new Proyecto (cod,titulo, fechaini, fechafin,costo);
        ControlProyecto objControlProyecto= new ControlProyecto (objProyecto);
        objProyecto = objControlProyecto.consultar();
        cod = objProyecto.getCod();
        titulo = objProyecto.getTitulo();
        fechaini = objProyecto.getFechaini();
        fechafin = objProyecto.getFechafin();
        costo = objProyecto.getCosto();
        txtcod.setText(String.valueOf(cod));
        txttitulo.setText(titulo);
        jdcfechaini.setDate(fechaini);
        jdcfechafin.setDate(fechafin);
        txtcosto.setText(String.valueOf(costo));
    }//GEN-LAST:event_jbnConsultarActionPerformed

    private void jbnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnBorrarActionPerformed
        Integer cod = Integer.valueOf(txtcod.getText());
        Proyecto objProyecto= new Proyecto (cod,"", null, null,0);
        ControlProyecto objControlProyecto= new ControlProyecto (objProyecto);
        objControlProyecto.borrar();
        JOptionPane.showConfirmDialog(null, "Registro modificado");
        txtcod.setText("");
        txttitulo.setText("");
        jdcfechaini.setDate(null);
        jdcfechafin.setDate(null);
        txtcosto.setText("");
    }//GEN-LAST:event_jbnBorrarActionPerformed

